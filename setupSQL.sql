  `CNAME` varchar(32) NOT NULL,
  `IUSERID` int(16) NOT NULL,
  `IISDEFAULT` int(1) NOT NULL,
  `DCREATEDDATE` datetime NOT NULL DEFAULT '0000-00-00 00:00:00',
  `DMODIFIEDDATE` datetime NOT NULL DEFAULT '0000-00-00 00:00:00',
